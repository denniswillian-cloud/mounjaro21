export function t(lang: Language, key: string, vars?: Record<string, string | number>): string {
  let text = translations[lang][key] ?? translations['es'][key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      text = text.replace(`{${k}}`, String(v));
    });
  }
  return text;
}
